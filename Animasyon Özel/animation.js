function animaiton(){
    document.getElementById("img").style.animation="none"
    document.getElementById("p").innerHTML="Animasyon Duraklatıldı"
    document.getElementById("p").style.display="block"
}

function animaitonstart(){
    document.getElementById("img").style.animation="images 6s infinite"
    document.getElementById("p").innerHTML="Animasyon Başlatıldı"
    document.getElementById("p").style.display="block"
}